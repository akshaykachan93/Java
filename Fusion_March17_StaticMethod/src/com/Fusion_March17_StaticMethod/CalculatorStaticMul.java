package com.Fusion_March17_StaticMethod;

public class CalculatorStaticMul {

	public static void mul() {

		System.out.println("Ram");
		System.out.println(20 * 30);

		System.out.println("Ram");
		System.out.println(20 * 30);

		System.out.println("Ram");
		System.out.println(20 * 30);

		System.out.println("Ram");
		System.out.println(20 * 30);
		
		System.out.println("----------------");
	}

	public static void main(String[] args) {
		mul();
		mul();
		mul();
		mul();
	}

}
