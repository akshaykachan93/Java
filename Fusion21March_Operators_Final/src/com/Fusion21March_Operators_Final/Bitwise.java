package com.Fusion21March_Operators_Final;

public class Bitwise {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		int c = 30;

		System.out.println(a > b & ++a < c); // true
		// F
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

		System.out.println("===================");

	}

}

