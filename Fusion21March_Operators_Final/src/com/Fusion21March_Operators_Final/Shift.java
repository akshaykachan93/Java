package com.Fusion21March_Operators_Final;

public class Shift {

	public static void main(String[] args) {

		System.out.println(10<<3);
		System.out.println(2<<4);
		
		System.out.println(100>>3);
		System.out.println(2>>4);
		
		int a=10;
		int b=-10;
		
		System.out.println(~a);
		System.out.println(~b);

	}

}